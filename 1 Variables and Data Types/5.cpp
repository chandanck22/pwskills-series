/*
Write a c++ program to find size of basic datatype.
*/


#include <iostream>
using namespace std;

int main()
{
    cout<<"Size of Char is: \t"<<sizeof(char) <<endl;
    cout<<"Size of int is: \t"<<sizeof(int) <<endl;
    cout<<"Size of short is: \t"<<sizeof(short) <<endl;
    cout<<"Size of double is: \t"<<sizeof(double) <<endl;
    cout<<"Size of long is: \t"<<sizeof(long) <<endl;
    cout<<"Size of long long is: \t"<<sizeof(long long) <<endl;
    cout<<"Size of float is: \t"<<sizeof(float) <<endl;
    cout<<"Size of double is: \t"<<sizeof(double) <<endl;
    cout<<"Size of long double is: "<<sizeof(long double) <<endl;
    cout<<"Size of bool is: \t"<<sizeof(bool) <<endl;

    return 0;
}
